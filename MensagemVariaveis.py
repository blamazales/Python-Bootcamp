# 9. Mensagem Amigável com Variáveis

# Definindo as variáveis
nome = input("Qual o seu nome? ")
cidade = input("Onde você mora? ")
hobby = input("O que você gosta de fazer no tempo livre? ")
tecnologia = input("Qual tecnologia você está estudando? ")

# Mensagem formatada
print(f"\nOlá, {nome}! Que legal saber que você mora em {cidade}.")
print(f"Eu também acho {hobby} incrível, mas o que mais me impressiona")
print(f"é o fato de você estar dominando {tecnologia}. Continue firme nos estudos!")